# Nuestro primer contacto con los tipos de datos.

print("--- 1. TIPO DE DATO: TEXTO (STRING) ---")
# Guardamos texto usando comillas. Así le decimos a Python que esto es lenguaje humano.
nombre_mascota = "Firulais"
print("El nombre de la mascota es:")
print(nombre_mascota)

print("--- 2. TIPO DE DATO: NÚMERO ENTERO (INT) ---")
# Guardamos un número real. ¡Observa cómo no usamos comillas!
edad_mascota = 5
print("La edad de la mascota en años es:")
print(edad_mascota)

# Usamos el símbolo '+' para realizar una suma matemática.
# Le sumamos 2 a la caja 'edad_mascota' y guardamos el resultado en una nueva caja.
edad_futura = edad_mascota + 2
print("En dos años, la mascota tendrá:")
print(edad_futura)

print("--- 3. TIPO DE DATO: BOOLEANO (BOOL) ---")
# Guardamos un estado de "encendido o apagado": ¿La mascota tiene hambre?
# Usamos 'True' (Verdadero). Debe tener la T en mayúscula y no llevar comillas.
tiene_hambre = True
print("¿La mascota tiene hambre en este momento?")
print(tiene_hambre)

print("--- FIN DEL EJEMPLO ---")
